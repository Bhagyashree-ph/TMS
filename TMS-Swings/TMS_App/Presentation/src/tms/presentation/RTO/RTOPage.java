    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed

            // TODO add your handling code here:
            String regNo = formattxtfldRegistrationNoToAdd.getText();
            if (regNo.isEmpty() || !(Validator.validateRegNo(regNo))) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Registration number", "Ivalid Registration Number", JOptionPane.ERROR_MESSAGE);
            return;
        }
            
            int vehicleID = Integer.parseInt(txtfldVehicleIDToAdd.getText());
            VehicleEO vehicle = null;
        try {
            vehicle = rto.fetchVehicleByVehicleID(vehicleID);
        } catch (Exception ex) {
            Logger.getLogger(RTOPage.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "No vehicle found with vehicle id "+vehicleID, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
            
            String OwnerID = txtfldOwnerIDToAdd.getText();
            OwnerEO owner = null;
        try {
            owner = rto.fetchOwnerByOwnerID(OwnerID);
        } catch (Exception ex) {
            Logger.getLogger(RTOPage.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "No Owner found with owner id "+OwnerID, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
            
            java.util.Date entregDate = jDateChooserRegDateToAdd.getDate();
            java.sql.Date regDate = new java.sql.Date(entregDate.getTime());
            
            RegistrationEO registration = new RegistrationEO(regNo, vehicle, owner, regDate);
            int rows = 0;
        try {
            rows = rto.registerVehicle(registration);
        } catch (Exception ex) {
            Logger.getLogger(RTOPage.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Some error occurred", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
            if (rows > 0){
                System.out.println("Registered vehicle");
                JOptionPane.showMessageDialog(this, "Vehicle registered successfully..!");
            }
            else{
                System.out.println("Vehicle not registered..");
                JOptionPane.showMessageDialog(this, "Some error occurred", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

