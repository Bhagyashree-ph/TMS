    private void addOffences() {
        try {
            ArrayList<OffenceTypeEO> offences = rto.fetchAllOffences();
            DefaultTableModel model = (DefaultTableModel) tableOffences.getModel();
            while (model.getRowCount() > 0) {
                model.removeRow(0);
            }
            for (OffenceTypeEO offence : offences) {
                model.addRow(
                        new Object[]{
                            offence.getOffenseID(),
                            offence.getOffenseName(),
                            offence.getVehicleType(),
                            offence.getPenalty()
                        }
                );
            }
        } catch (Exception ex) {
            Logger.getLogger(RTOPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
