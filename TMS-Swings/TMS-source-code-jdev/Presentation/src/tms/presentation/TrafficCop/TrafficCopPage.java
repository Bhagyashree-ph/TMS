    private void btnClearOffenceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearOffenceActionPerformed
        
            // TODO add your handling code here:
            String entRegNo = txtfldSearchHistoryToClearByCop.getText();
            if (entRegNo.isEmpty() || !(Validator.validateRegNo(entRegNo))) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Registration number", "Ivalid Registration Number", JOptionPane.ERROR_MESSAGE);
            return;
            }
            ArrayList<OffenceDetailEO> details;
        try {
            details = cop.fetchAllOffencesCommited(entRegNo);
        
            int count = 0;
            int row = 0;
            for (OffenceDetailEO detail : details) {
                if (detail.getPenaltyStatus().equalsIgnoreCase("Unpaid")) {
                    boolean isSelected = (boolean) tableViewHistoryToClearByCaop.getValueAt(row++, 0);
                    if (isSelected) {
                        detail.setPenaltyStatus("Paid");
                        cop.clearOffense(detail);
                        count++;
                    }
                }
            }
            if (count > 0) {
                System.out.println(count + " details updated..");
                JOptionPane.showMessageDialog(this, count + " details updated");
                clear(entRegNo);
            } else {
                System.out.println("No details found..!");
                JOptionPane.showMessageDialog(this, "No details found..!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(TrafficCopPage.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Some error occurred while updating.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnClearOffenceActionPerformed
