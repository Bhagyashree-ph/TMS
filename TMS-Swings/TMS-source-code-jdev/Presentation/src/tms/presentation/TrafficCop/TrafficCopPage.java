    private void togBtnViewHistoryToViewByCopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_togBtnViewHistoryToViewByCopActionPerformed

            // TODO add your handling code here:
            String regNo = txtfldSearchHistoryToViewByCop.getText();
            if(!Validator.validateRegNo(regNo) || regNo.isEmpty())
                JOptionPane.showMessageDialog(this, "Invalid registration number", "Invalid input", JOptionPane.ERROR_MESSAGE);
            ArrayList<OffenceDetailEO> offDetails = null;
            try {
                offDetails = cop.fetchAllOffencesCommited(regNo);
            DefaultTableModel model = (DefaultTableModel) tableViewHistoryToViewByCaop.getModel();
            while (model.getRowCount() != 0) {
                model.removeRow(0);
            }
            int count = 1;
            for (OffenceDetailEO offenceDetailEO : offDetails) {
                model.addRow(new Object[]{
                    count,
                    offenceDetailEO.getOffenseDetailID(),
                    offenceDetailEO.getOffenseDateTime(),
                    offenceDetailEO.getPlace(),
                    offenceDetailEO.getImage(),
                    offenceDetailEO.getRegistration().getRegistrationID(),
                    offenceDetailEO.getOffense().getOffenseID(),
                    offenceDetailEO.getUser().getUserName(),
                    offenceDetailEO.getPenaltyStatus()
                });
                count++;
            }
        } catch (Exception ex) {
            Logger.getLogger(TrafficCopPage.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "No Offences found..!", "Not found", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_togBtnViewHistoryToViewByCopActionPerformed

